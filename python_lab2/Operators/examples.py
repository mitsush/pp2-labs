# Arithmetic Operators
"""
+	Addition
-	Subtraction
*	Multiplication
/	Division	x / y
%	Modulus	x % y
**	Exponentiation
//	Floor division
"""

# Assignment Operators
"""
=	x = 5	x = 5
+=	x += 3	x = x + 3
-=	x -= 3	x = x - 3
*=	x *= 3	x = x * 3
/=	x /= 3	x = x / 3
%=	x %= 3	x = x % 3
//=	x //= 3	x = x // 3
**=	x **= 3	x = x ** 3
&=	x &= 3	x = x & 3
|=	x |= 3	x = x | 3
^=	x ^= 3	x = x ^ 3
>>=	x >>= 3	x = x >> 3
<<=	x <<= 3	x = x << 3
"""

# Comparison Operators
"""
==	Equal	                        x == y
!=	Not equal	                x != y
>	Greater than	                x > y
<	Less than	                x < y
>=	Greater than or equal to	x >= y
<=	Less than or equal to	        x <= y
"""

# Operator Precedence
print((6 + 3) - (6 + 3))

